package ru.tardyon.maven.telegram.notifier.core.dispatch;

/**
 * Режимы форматирования текста сообщений Telegram.
 *
 * <p>Определяет способ интерпретации текста при отправке уведомлений в Telegram.
 * Различные режимы позволяют использовать разную разметку для форматирования сообщений.
 *
 * <p>Доступные режимы:
 * <ul>
 *   <li>{@link #PLAIN} - обычный текст без форматирования</li>
 *   <li>{@link #MARKDOWN} - разметка Markdown (устаревший формат)</li>
 *   <li>{@link #MARKDOWN_V2} - разметка MarkdownV2 (улучшенный формат)</li>
 *   <li>{@link #HTML} - разметка HTML</li>
 * </ul>
 */
public enum ParseMode {
    /**
     * Обычный текст без форматирования.
     *
     * <p>Текст отправляется как есть, без применения какой-либо разметки.
     * Все специальные символы отображаются в исходном виде.
     */
    PLAIN,
    /**
     * Форматирование с использованием Markdown.
     *
     * <p>Поддерживает базовую разметку Markdown (устаревший формат Telegram).
     * Позволяет использовать жирный текст, курсив, ссылки и другие элементы форматирования.
     *
     * @see #MARKDOWN_V2
     */
    MARKDOWN,
    /**
     * Форматирование с использованием MarkdownV2.
     *
     * <p>Улучшенная версия Markdown с расширенными возможностями форматирования.
     * Требует экранирования специальных символов для корректного отображения.
     */
    MARKDOWN_V2,
    /**
     * Форматирование с использованием HTML-разметки.
     *
     * <p>Поддерживает подмножество HTML-тегов для форматирования текста.
     * Позволяет использовать теги типа &lt;b&gt;, &lt;i&gt;, &lt;a&gt; и другие.
     */
    HTML
}
