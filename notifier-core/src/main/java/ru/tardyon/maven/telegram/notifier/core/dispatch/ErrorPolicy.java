package ru.tardyon.maven.telegram.notifier.core.dispatch;

/**
 * Политика обработки ошибок при отправке уведомлений в Telegram.
 *
 * <p>Определяет, как система должна реагировать на ошибки, возникающие
 * при попытке отправить уведомление через Telegram Bot API.
 */
public enum ErrorPolicy {
    /**
     * Только логировать ошибку.
     *
     * <p>При возникновении ошибки отправки уведомления будет записана в лог,
     * но исключение не будет пробрасываться дальше. Основной метод продолжит
     * нормальное выполнение.
     */
    LOG_ONLY,

    /**
     * Пробросить исключение.
     *
     * <p>При возникновении ошибки отправки уведомления исключение будет
     * проброшено дальше, что может прервать выполнение основного метода.
     */
    THROW
}
