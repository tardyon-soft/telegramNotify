package ru.tardyon.maven.telegram.notifier.core.annotation;

/**
 * Определяет момент отправки уведомления относительно выполнения метода.
 *
 * <p>Используется в аннотации {@link TelegramNotify} для указания, когда именно
 * должно быть отправлено уведомление в Telegram.
 */
public enum NotifyWhen {
    /**
     * Отправить уведомление перед выполнением метода.
     *
     * <p>Уведомление отправляется до того, как начнется выполнение аннотированного метода.
     */
    BEFORE,

    /**
     * Отправить уведомление после успешного выполнения метода.
     *
     * <p>Уведомление отправляется только если метод завершился без исключений.
     * Доступна переменная {@code #result} с возвращаемым значением метода.
     */
    AFTER_SUCCESS,

    /**
     * Отправить уведомление при возникновении исключения в методе.
     *
     * <p>Уведомление отправляется только если метод завершился с исключением.
     * Доступна переменная {@code #ex} с информацией об исключении.
     */
    AFTER_FAILURE,

    /**
     * Отправить уведомление после выполнения метода в любом случае.
     *
     * <p>Уведомление отправляется независимо от того, завершился метод успешно или с ошибкой.
     * Доступны переменные {@code #result} (может быть null) и {@code #ex} (может быть null).
     */
    AFTER_FINALLY
}
